import turtle as t
t.shape("turtle")
t.penup()
t.setpos(-100,-50)
t.pendown()
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.penup()
t.setpos(0, -50)
t.pendown()
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.done()
